var lastStoneWeight = function (stones) {
  while (stones.length > 1) {
    stones = stones.sort((a,b) => a - b);
    const Stone1 = stones.pop();
    const Stone2 = stones.pop();

    if (Stone1 !== Stone2) {
      stones.push(Math.abs(Stone1 - Stone2));
    }
  }
  return stones[0] ? stones[0] : 0;
};

